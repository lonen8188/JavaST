package ch02;

public class Intexam {

	public static void main(String[] args) {
		// int 타입의 특징
		// -21억 ~ 21억 값이 범위를 표현
		// 10진~ 2진까지 표현가능
		
		int var1 = 10 ;     // 10진수
		int var2 = 012 ;    // 8진수
		int var3 = 0xA ;    // 16진수
		int var4 = 0b0010 ; // 2진수
		
		System.out.println(var1);
		System.out.println(var2);
		System.out.println(var3);
		System.out.println(var4);

	}

}

