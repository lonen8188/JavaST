package ch05;

public class ForExam {

	public static void main(String[] args) {
		// for문은 반복되는 함수로 최대값을 가지고 있다.
		// for(초기화식 ; 조건식 ; 증감식){ 실행문 }

		for(int i = 0 ; i <= 10 ; i = i + 2 ) {
			//반복시 실행문
			System.out.println("1~10까지 반복 출력 : " + i );
			
		} // for문 종료
	} // main() 종료

} // class 종료
